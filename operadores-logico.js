console.log('aula de operadores logico')

// > maior que
console.log(10 > 9);

// < menor que
console.log(10 < 9);

// >= maior ou igual
console.log(10 >= 9);

// <= menor ou igual
console.log(10 <= 9);

// != (diferente)
console.log(10 != 9);

// && e (os dois lados da expressao precisam ser verdadeiros)
console.log();
console.log((10 > 8) && (10 < 9));

// || ou (apenas um lado da expressao precisa ser verdadeiro)
console.log();
console.log((10 > 8) || (10 < 9));
